function [axmat, aymat, axxmat, axymat, ayymat]=setupmatrices2fast(nums, numt, dx, dy)
%
% sparse matrix representations of derivatives
%
%
nxcs=nums*numt;
xiv=zeros(2*nxcs, 1);
xjv=zeros(2*nxcs, 1);
xvv=zeros(2*nxcs, 1);
xp=1;
yiv=zeros(2*nxcs, 1);
yjv=zeros(2*nxcs, 1);
yvv=zeros(2*nxcs, 1);
yp=1;
xxiv=zeros(3*nxcs, 1);
xxjv=zeros(3*nxcs, 1);
xxvv=zeros(3*nxcs, 1);
xxp=1;
yyiv=zeros(3*nxcs, 1);
yyjv=zeros(3*nxcs, 1);
yyvv=zeros(3*nxcs, 1);
yyp=1;
xyiv=zeros(4*nxcs, 1);
xyjv=zeros(4*nxcs, 1);
xyvv=zeros(4*nxcs, 1);
xyp=1;
ptr=1;
for i=1:nums
  for j=1:numt
    ptr=(j-1)*nums+i;
    if i>1 && i<nums
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i;
        xvv(xp)=-1/dx;
        xp=xp+1;
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i+1;
        xvv(xp)=1/dx;
        xp=xp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i-1;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i;
        xxvv(xxp)=-2/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i+1;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
%       axmat(ptr, (j-1)*nums+i)=-1/(dx);
%       axmat(ptr, (j-1)*nums+i+1)=1/(dx);
%       axxmat(ptr, (j-1)*nums+i-1)=1/dx^2;
%       axxmat(ptr, (j-1)*nums+i)=-2/dx^2;
%       axxmat(ptr, (j-1)*nums+i+1)=1/dx^2;
    elseif i>1
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i-1;
        xvv(xp)=-1/dx;
        xp=xp+1;
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i;
        xvv(xp)=1/dx;
        xp=xp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i-2;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i-1;
        xxvv(xxp)=-2/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
%       axmat(ptr, (j-1)*nums+i-1)=-1/dx;
%       axmat(ptr, (j-1)*nums+i)=1/dx;
%       axxmat(ptr, (j-1)*nums+i-2)=1/dx^2;
%       axxmat(ptr, (j-1)*nums+i-1)=-2/dx^2;
%       axxmat(ptr, (j-1)*nums+i)=1/dx^2;
    elseif i<nums
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i;
        xvv(xp)=-1/dx;
        xp=xp+1;
        xiv(xp)=ptr;
        xjv(xp)=(j-1)*nums+i+1;
        xvv(xp)=1/dx;
        xp=xp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i+1;
        xxvv(xxp)=-2/dx^2;
        xxp=xxp+1;
        xxiv(xxp)=ptr;
        xxjv(xxp)=(j-1)*nums+i+2;
        xxvv(xxp)=1/dx^2;
        xxp=xxp+1;
%       axmat(ptr, (j-1)*nums+i)=-1/dx;
%       axmat(ptr, (j-1)*nums+i+1)=1/dx;
%       axxmat(ptr, (j-1)*nums+i)=1/dx^2;
%       axxmat(ptr, (j-1)*nums+i+1)=-2/dx^2;
%       axxmat(ptr, (j-1)*nums+i+2)=1/dx^2;

    end
    if j>1 && j<numt
        yiv(yp)=ptr;
        yjv(yp)=(j-1)*nums+i;
        yvv(yp)=-1/dy;
        yp=yp+1;
        yiv(yp)=ptr;
        yjv(yp)=(j)*nums+i;
        yvv(yp)=1/dy;
        yp=yp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-2)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-1)*nums+i;
        yyvv(yyp)=-2/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
%       aymat(ptr, (j-1)*nums+i)=-1/(dy);
%       aymat(ptr, (j)*nums+i)=1/(dy);
%       ayymat(ptr, (j-2)*nums+i)=1/dy^2;
%       ayymat(ptr, (j-1)*nums+i)=-2/dy^2;
%       ayymat(ptr, (j)*nums+i)=1/dy^2;
    elseif j>1
        yiv(yp)=ptr;
        yjv(yp)=(j-2)*nums+i;
        yvv(yp)=-1/dy;
        yp=yp+1;
        yiv(yp)=ptr;
        yjv(yp)=(j-1)*nums+i;
        yvv(yp)=1/dy;
        yp=yp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-3)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-2)*nums+i;
        yyvv(yyp)=-2/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-1)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
%       aymat(ptr, (j-2)*nums+i)=-1/dy;
%       aymat(ptr, (j-1)*nums+i)=1/dy;
%       ayymat(ptr, (j-3)*nums+i)=1/dy^2;
%       ayymat(ptr, (j-2)*nums+i)=-2/dy^2;
%       ayymat(ptr, (j-1)*nums+i)=1/dy^2;
    elseif j<numt
        yiv(yp)=ptr;
        yjv(yp)=(j-1)*nums+i;
        yvv(yp)=-1/dy;
        yp=yp+1;
        yiv(yp)=ptr;
        yjv(yp)=(j)*nums+i;
        yvv(yp)=1/dy;
        yp=yp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j-1)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j)*nums+i;
        yyvv(yyp)=-2/dy^2;
        yyp=yyp+1;
        yyiv(yyp)=ptr;
        yyjv(yyp)=(j+1)*nums+i;
        yyvv(yyp)=1/dy^2;
        yyp=yyp+1;
%       aymat(ptr, (j-1)*nums+i)=-1/dy;
%       aymat(ptr, (j)*nums+i)=1/dy;
%       ayymat(ptr, (j-1)*nums+i)=1/dy^2;
%       ayymat(ptr, (j)*nums+i)=-2/dy^2;
%       ayymat(ptr, (j+1)*nums+i)=1/dy^2;
    end
    % four cases for mixed second derivatives
    %
    %  mask is 
    if (i>1) && (i<nums) && (j>1) && (j<numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i-1;
        xyvv(xyp)=-1/(4*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i+1;
        xyvv(xyp)=-1/(4*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i+1;
        xyvv(xyp)=1/(4*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i-1;
        xyvv(xyp)=1/(4*dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i-1)=-1/(4*dx*dy);
%       axymat(ptr, (j)*nums+i+1)=-1/(4*dx*dy);
%       axymat(ptr, (j-2)*nums+i+1)=1/(4*dx*dy);
%       axymat(ptr, (j)*nums+i-1)=1/(4*dx*dy);
    elseif (i==1) && (j>1) && (j<numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i+1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i+1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i)=-1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i+1)=-1/(2*dx*dy);
%       axymat(ptr, (j-2)*nums+i+1)=1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i)=1/(2*dx*dy);
    elseif (i==nums) && (j>1) && (j<numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i-1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i-1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i-1)=-1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i)=-1/(2*dx*dy);
%       axymat(ptr, (j-2)*nums+i)=1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i-1)=1/(2*dx*dy);
     elseif (i>1) && (i<nums) && (j==1)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i-1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i+1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i+1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i-1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-1)*nums+i-1)=-1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i+1)=-1/(2*dx*dy);
%       axymat(ptr, (j-1)*nums+i+1)=1/(2*dx*dy);
%       axymat(ptr, (j)*nums+i-1)=1/(2*dx*dy);
    elseif (i>1) && (i<nums) && (j==numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i-1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i+1;
        xyvv(xyp)=-1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i+1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i-1;
        xyvv(xyp)=1/(2*dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i-1)=-1/(2*dx*dy);
%       axymat(ptr, (j-1)*nums+i+1)=-1/(2*dx*dy);
%       axymat(ptr, (j-2)*nums+i+1)=1/(2*dx*dy);
%       axymat(ptr, (j-1)*nums+i-1)=1/(2*dx*dy);
    elseif (i==1) && (j==1)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i+1;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i+1;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-1)*nums+i)=-1/(dx*dy);
%       axymat(ptr, (j)*nums+i+1)=-1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i+1)=1/(dx*dy);
%       axymat(ptr, (j)*nums+i)=1/(dx*dy);
    elseif (i==1) && (j==numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i+1;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i+1;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i)=-1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i+1)=-1/(dx*dy);
%       axymat(ptr, (j-2)*nums+i+1)=1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i)=1/(dx*dy);
    elseif (i==nums) && (j==1)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i-1;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j)*nums+i-1;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-1)*nums+i-1)=-1/(dx*dy);
%       axymat(ptr, (j)*nums+i)=-1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i)=1/(dx*dy);
%       axymat(ptr, (j)*nums+i-1)=1/(dx*dy);
    elseif (i==nums) && (j==numt)
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i-1;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i;
        xyvv(xyp)=-1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-2)*nums+i;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
        xyiv(xyp)=ptr;
        xyjv(xyp)=(j-1)*nums+i-1;
        xyvv(xyp)=1/(dx*dy);
        xyp=xyp+1;
%       axymat(ptr, (j-2)*nums+i-1)=-1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i)=-1/(dx*dy);
%       axymat(ptr, (j-2)*nums+i)=1/(dx*dy);
%       axymat(ptr, (j-1)*nums+i-1)=1/(dx*dy);
    else
      error('nonexistent case in setupmatrices');
    end
  end
end
axmat=sparse(xiv(1:xp-1), xjv(1:xp-1), xvv(1:xp-1), nxcs, nxcs);
aymat=sparse(yiv(1:yp-1), yjv(1:yp-1), yvv(1:yp-1), nxcs, nxcs);
axxmat=sparse(xxiv(1:xxp-1), xxjv(1:xxp-1), xxvv(1:xxp-1), nxcs, nxcs);
axymat=sparse(xyiv(1:xyp-1), xyjv(1:xyp-1), xyvv(1:xyp-1), nxcs, nxcs);
ayymat=sparse(yyiv(1:yyp-1), yyjv(1:yyp-1), yyvv(1:yyp-1), nxcs, nxcs);


    
